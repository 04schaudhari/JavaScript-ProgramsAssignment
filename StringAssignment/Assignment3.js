// 3. WAP to print or log the first character of the given strings
//  ○ const str = “React”
//  ○ const str = “Elon Musk”
//  ○ const str =  “Apple founder is Stew Job”

function firstCharacter(str){

    return str.charAt(0);
 
}

const str1 = "React"
console.log(`Given String is : ${str1} => The First Character of this String is: ${firstCharacter(str1)}`);

const str2 = "Elon Musk"
console.log(`Given String is : ${str2} => The First Character of this String is: ${firstCharacter(str2)}`);

const str3 = "Apple founder is Stew Job"
console.log(`Given String is : ${str3} => The First Character of this String is: ${firstCharacter(str3)}`);

